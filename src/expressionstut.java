public class expressionstut { // Define a public class named 'expressionstut'

    public static void main(String[] args) { // Main method, the entry point of the program

        int friends = 10; // Declare an integer variable 'friends' and assign it the value 10

        // Increment the value of 'friends' by 1 using the addition assignment operator
        // The expression 'friends + 1' adds 1 to the current value of 'friends',
        // and the result is assigned back to the variable 'friends'
        friends = friends + 1;

        // Print the updated value of 'friends'
        // Expected output: 11, since we incremented the value by 1
        System.out.println(friends); // Output: 11
    }
}
