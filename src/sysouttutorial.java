public class sysouttutorial { // Define a public class named 'sysouttutorial'
    public static void main(String[] args) { // Main method, the entry point of the program

        // Print "i love pizza" followed by a new line
        System.out.println("i love pizza");

        // Print a tab character followed by "its really good" and then a new line
        System.out.println("\tits really good");

        // Print a blank line
        System.out.println();

        // Explanation of escape sequences:
        // Escape sequences start with a backslash (\) and are used to represent special characters in a string.

        // \n is used for a new line
        // \t is used for a tab space
        // \\ is used to display a backslash
        // \" is used to include double quotes within a string

        // Example of displaying a backslash
        System.out.println("\\this is how u display a backslash");

        // Example of quoting text within the output
        System.out.println("\"This is an example of quoting a text\"");

        // Example of a multi-line comment using /* ... */
        /*
        here
        is
        a
        example
        */
    }
}

